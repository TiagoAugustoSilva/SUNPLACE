    <script>
        document.addEventListener("DOMContentLoaded", function() {
            // Elementos do chat
            var chatContainer = document.getElementById("chat-container");
            var chatMessages = document.getElementById("chat-messages");
            var chatInput = document.getElementById("chat-input");
            var openChatIcon = document.getElementById("open-chat-icon"); // Aqui está a mudança

            // Frases de resposta do atendente
            var responses = [
                "Atendente: Olá Seja bem vindo Obrigado por entrar em contato! Como posso ajudar?",
                "Atendente: Fico feliz em ajudar! Qual é a sua dúvida?",
                "Atendente: Deseja tirar mais alguma dúvida?",
                "Atendente: Poderia avaliar o atendimento com uma nota de 1 a 5 ?",


                // Adicione mais frases aqui conforme necessário
            ];

            // Função para abrir o chat
            function openChat() {
                chatContainer.style.display = "block";
            }

            // Função para fechar o chat
            function closeChat() {
                chatContainer.style.display = "none";
            }

            // Função para enviar mensagens
            function sendMessage() {
                var message = chatInput.value.trim();
                if (message !== "") {
                    var messageElement = document.createElement("div");
                    messageElement.textContent = "Você: " + message;
                    chatMessages.appendChild(messageElement);
                    chatInput.value = "";
                    // Simulação de resposta do atendente
                    setTimeout(function() {
                        var randomIndex = Math.floor(Math.random() * responses.length);
                        var response = responses[randomIndex];
                        var responseElement = document.createElement("div");
                        responseElement.textContent = response;
                        chatMessages.appendChild(responseElement);
                        chatMessages.scrollTop = chatMessages.scrollHeight;
                    }, 1000);
                }
            }

            // Adiciona evento para enviar mensagem ao pressionar Enter
            chatInput.addEventListener("keypress", function(event) {
                if (event.keyCode === 13) {
                    event.preventDefault();
                    sendMessage();
                }
            });

            // Adiciona evento para abrir o chat ao clicar no ícone de suporte
            openChatIcon.addEventListener("click", function(event) {
                event.preventDefault();
                openChat();
            });

            // Adiciona evento para fechar o chat ao clicar no botão de fechar
            var closeChatButton = document.getElementById("close-chat-button");
            closeChatButton.addEventListener("click", function(event) {
                event.preventDefault();
                closeChat();
            });
        });

        document.addEventListener("DOMContentLoaded", function() {
            // Função para confirmar a saída antes de submeter o formulário
            function confirmLogout(event) {
                var confirmation = confirm("Tem certeza que deseja sair?");
                if (!confirmation) {
                    event.preventDefault(); // Cancela a submissão do formulário se o usuário cancelar
                }
            }

            // Adiciona o evento de clique ao formulário de logout
            var logoutForm = document.getElementById("logout-form");
            logoutForm.addEventListener("submit", confirmLogout);
        });
    </script>
